(
	[ticket id] nvarchar(10) primary key,
	[cashier id] nvarchar(10) references Cashier([cashier id]),
	[block id] nvarchar(10) references Block([block id]),
	[entry date] date,
	[exit date] date,
	[vehicle id] nvarchar(10) references Vehicle([vehicle id])
