create table Vehicle
(
	[vehicle id] nvarchar(10) primary key,
	[manufacturer] nvarchar(40),
	[model] nvarchar(100),
)
create table Pricelist
(
	[typeid] nvarchar(10) primary key,
	[service] nvarchar(100),
	[price] float
)
create table Invoice
(
	[invoice id] nvarchar(10) primary key,
	[cashier id] nvarchar(10) references Cashier([cashier id]),
	[engineer id] nvarchar(10) references Engineer([engineer id]),
	[typeid] nvarchar(10) references Pricelist([typeid]),
	[vehicle id] nvarchar(10) references Vehicle([vehicle id]),
	[date] date
