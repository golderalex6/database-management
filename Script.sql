--trigger
create trigger tr_parking_on on Parking
after insert
as
begin
	update [Block] set [state]=1 where [block id]=(select [block id] from inserted)
end
go

create trigger tr_parking_off on Parking
after delete
as
begin
	declare @blockid nvarchar(10)
	select @blockid=[block id] from deleted
	update [Block]
	set [state]=0 where [block id]=@blockid
end

go

--------------------------------------------------------------------Function
create function check_block(@blockid nvarchar(10))
returns int
as
begin
	declare @blockstate int
	select @blockstate=[state] from [Block] where  [block id]=@blockid
	return @blockstate
end

go

create function count_parking_garage(@garageid nvarchar(10))
returns int
as
begin
	declare @total int
	select @total=count(*) from Parking
	join Block on Parking.[block id]=Block.[block id]
	join Garage on Block.[garage id]=Garage.[garage id]
	where Garage.[garage id]=@garageid
	return @total
end

go

create function profit_a_day(@date date)
returns float
as
begin
	declare @profit float,@total_day int,@profit_invoice float
	
	select @total_day=DATEDIFF(DAY,[entry date],[exit date]) from Ticket where [exit date]=@date

	if @total_day is null
		set @total_day=0
	select @profit_invoice=sum(Pricelist.price) from Invoice
	join Pricelist on Invoice.typeid=Pricelist.typeid
	where date=@date

	set @profit=@profit_invoice+@total_day*2
	return @profit
end

