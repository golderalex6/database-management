--------------------------------------------------------------------Procedure

create procedure park
@manufacturor nvarchar(50),@model nvarchar(100),@blockid nvarchar(10)
as
begin
	declare @vehicleid nvarchar(10)
	select top 1 @vehicleid=[vehicle id] from Vehicle order by [vehicle id] desc
	set @vehicleid='V/'+convert(nvarchar(100),convert(int,SUBSTRING(@vehicleid,3,len(@vehicleid)-2))+1)
	if dbo.check_block(@blockid)=0
		begin
			insert into Vehicle values(@vehicleid,@manufacturor,@model)
			insert into Parking values(@vehicleid,GETDATE(),@blockid)
		end
	else
		print 'Already Placed,please change to another block.'
end

go

create procedure takecar
@vehicleid nvarchar(10)
as
begin
	declare @ticketid nvarchar(10),@cashierid nvarchar(10),@blockid nvarchar(10),@entrydate date

	select top 1 @cashierid=Cashier.[cashier id] from Parking
	join [Block] on Parking.[block id]=[Block].[block id]
	join Garage on [Block].[garage id]=Garage.[garage id]
	join Manager on Manager.[garage id]=Garage.[garage id]
	join Cashier on Manager.[manager id]=Cashier.[manager id]
	where Parking.[vehicle id]=@vehicleid

	select top 1 @ticketid=[ticket id] from Ticket order by [ticket id] desc

	set @ticketid='T/'+convert(nvarchar(100),convert(int,SUBSTRING(@ticketid,3,len(@ticketid)-2))+1)

	select @entrydate=[entry date],@blockid=[block id] from Parking where [vehicle id]=@vehicleid

	delete from Parking where [vehicle id]=@vehicleid

	insert into Ticket values(@ticketid,@cashierid,@blockid,@entrydate,GETDATE(),@vehicleid)
end

go

create procedure maintance
@manufacturor nvarchar(100),@model nvarchar(100),@typeid nvarchar(10),@garageid nvarchar(10)
as
begin
	declare @invoiceid nvarchar(10),@vehicleid nvarchar(10),@engineerid nvarchar(10),@cashierid nvarchar(10),@count int
	select top 1 @vehicleid=[vehicle id] from Vehicle order by [vehicle id] desc
	select top 1 @invoiceid=[invoice id] from Invoice order by [invoice id] desc

	set @vehicleid='V/'+convert(nvarchar(100),convert(int,SUBSTRING(@vehicleid,3,len(@vehicleid)-2))+1)
	set @invoiceid='I/'+convert(nvarchar(100),convert(int,SUBSTRING(@vehicleid,3,len(@vehicleid)-2))+1)

	select @engineerid=Invoice.[engineer id],@count=count(*) from Invoice 
	join Engineer on Invoice.[engineer id]=Engineer.[engineer id]
	where [manager id]=(select [manager id] from Manager where [garage id]=@garageid)
	group by Invoice.[engineer id]
	order by count(*)

	select top 1 @cashierid=[cashier id] from Cashier where [manager id]=(select [manager id] from Manager where [garage id]='G/000') order by [cashier id] desc

	insert into Vehicle values(@vehicleid,@manufacturor,@model)
	insert into Invoice values(@invoiceid,@cashierid,@engineerid,@typeid,@vehicleid,GETDATE())
end

